class Solution(object):
    def containsNearbyDuplicate(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: bool
        """
        dct_nums = {}
        for i in range(len(nums)):
            if dct_nums.get(nums[i]) is not None and i - dct_nums[nums[i]] <= k:
                return True
            dct_nums[nums[i]] = i
        return False


